    protected function getEntityId(object $entity): ?int
    {
        if ($entity instanceof Model) {
            return $entity->id ?? null;
        }

        if (property_exists($entity, 'id')) {
            return $entity->id;
        }

        if (method_exists($entity, 'getId')) {
            return $entity->getId();
        }

        return null;
    }
